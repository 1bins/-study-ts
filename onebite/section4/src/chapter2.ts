// 2-2. 매개변수의 개수가 다를 때
type Func1 = (a: number, b: number) => void;
type Func2 = (a: number) => void;

let func1: Func1 = (a, b) => {};
let func2: Func2 = (a) => {};

func1 = func2;
// func2 = func1; ❌ 위에 있는 예제와 같은 느낌, 매개변수가 더 큰 쪽 = 더 작은 쪽 가능